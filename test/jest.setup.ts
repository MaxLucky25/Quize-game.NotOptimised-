// Jest setup file для установки переменных окружения
import { join } from 'path';

// Принудительно устанавливаем переменные окружения для тестов
process.env.NODE_ENV = 'testing';
process.env.ENV_FILE_PATH = join('env', '.env.testing');

// Увеличиваем таймаут для всех тестов (30 секунд)
// Это важно для E2E тестов, которые могут занимать больше времени из-за инициализации БД
// Unit тесты тоже получат этот таймаут, но это не проблема - они завершатся быстрее
jest.setTimeout(30000);
